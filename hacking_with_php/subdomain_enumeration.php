function enumerateSubdomains($domain, $dictionary) {
    $subdomains = array();

    foreach ($dictionary as $subdomain) {
        $fullDomain = $subdomain . '.' . $domain;

        // Check if the subdomain exists
        $ip = gethostbyname($fullDomain);
        if ($ip != $fullDomain) {
            $subdomains[] = $fullDomain;
            echo "Found subdomain: $fullDomain\n";
        } else {
            echo "Subdomain not found: $fullDomain\n";
        }
    }

    return $subdomains;
}

